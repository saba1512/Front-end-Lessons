# рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃћрЃџрЃў рЃерЃћрЃўрЃДрЃЋрЃљрЃюрЃА рЃарЃўрЃфрЃ«рЃЋрЃА. рЃЊрЃљрЃЉрЃћрЃГрЃЊрЃћ рЃЏрЃўрЃАрЃў рЃюрЃљрЃЏрЃарЃљрЃЋрЃџрЃў 1-рЃЊрЃљрЃю 10-рЃЏрЃЊрЃћ.
# ­ЪЉЅ рЃЏрЃљрЃњрЃљрЃџрЃўрЃЌрЃљрЃЊ (рЃарЃўрЃфрЃ«рЃЋрЃў 3):
# 3 x 1 = 3
# 3 x 2 = 6
# ...
# 3 x 10 = 30

number = int(input("enter number: ")) # рЃћрЃЎрЃўрЃЌрЃ«рЃћрЃЉрЃљ рЃЏрЃЮрЃЏрЃ«рЃЏрЃљрЃарЃћрЃЉрЃћрЃџрЃА рЃарЃўрЃфрЃ«рЃЋрЃА

for i in range(1, 11): # 1-рЃЊрЃљрЃю 10-рЃЏрЃЊрЃћ
    print(number, "x", i, "=", number * i) # рЃЊрЃљрЃЉрЃћрЃГрЃЊрЃљрЃЋрЃА рЃарЃўрЃфрЃ«рЃЋрЃА, i-рЃА рЃЊрЃљ рЃЏрЃљрЃЌ рЃюрЃљрЃЏрЃарЃљрЃЋрЃџрЃА